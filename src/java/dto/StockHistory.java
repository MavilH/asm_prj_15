package dto;

import java.sql.Date;

/**
 *
 * @author Admin
 */
public class StockHistory {
    private int id;
    private String ticker;
    Date date;
    Date open;
    Date close;
    float high;
    float low;

    public StockHistory() {
    }

    public StockHistory(int id, String ticker, Date date, Date open, Date close, float high, float low) {
        this.id = id;
        this.ticker = ticker;
        this.date = date;
        this.open = open;
        this.close = close;
        this.high = high;
        this.low = low;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTicker() {
        return ticker;
    }

    public void setTicker(String ticker) {
        this.ticker = ticker;
    }

    public Date getDate() {
        return date;
    }

    public void setDate(Date date) {
        this.date = date;
    }

    public Date getOpen() {
        return open;
    }

    public void setOpen(Date open) {
        this.open = open;
    }

    public Date getClose() {
        return close;
    }

    public void setClose(Date close) {
        this.close = close;
    }

    public float getHigh() {
        return high;
    }

    public void setHigh(float high) {
        this.high = high;
    }

    public float getLow() {
        return low;
    }

    public void setLow(float low) {
        this.low = low;
    }

   
    
}
